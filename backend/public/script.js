// This will check if Metamask is installed and if Web3 is available
function checkMetamask() {
  if (window.ethereum) {
    console.log("Metamask is installed!");
    return true;
  } else {
    console.log("Please install Metamask!");
    alert("Please install Metamask!");
    return false;
  }
}

// Connect wallet to Metamask
async function connectWallet() {
  if (checkMetamask()) {
    try {
      const web3 = new Web3(window.ethereum);
      await window.ethereum.request({ method: 'eth_requestAccounts' });
      const accounts = await web3.eth.getAccounts();
      console.log(`Connected Wallet: ${accounts[0]}`);
      alert(`Connected Wallet: ${accounts[0]}`);
      return accounts[0];  // Return the user's address
    } catch (error) {
      console.error('Error connecting to Metamask:', error);
      alert("Failed to connect to wallet.");
    }
  }
}

// Trigger the wallet connection when the user clicks the button
document.getElementById('connect').addEventListener('click', async () => {
  await connectWallet();
  // Show the ad section after connecting wallet
  document.getElementById('ad-container').style.display = 'block';
  loadAd();  // Load a random ad after connection
});

// Load a random ad (image only, no video for simplicity)
function loadAd() {
  const adImages = [
    '/images/ad1.jpg',
    '/images/ad2.jpg',
    '/images/ad3.jpg',
    '/images/ad4.jpg'
  ];

  const randomAd = adImages[Math.floor(Math.random() * adImages.length)];
  const adThumbnail = document.getElementById('ad-thumbnail');
  if (adThumbnail) {
    adThumbnail.src = randomAd;  // Set image source for the ad
    adThumbnail.style.display = 'block';
    adThumbnail.style.cursor = 'pointer';  // Make the image clickable
    console.log('Ad image loaded:', randomAd);  // Debug log
  } else {
    console.error('Ad thumbnail element not found!');
  }
}

// Reward the user for watching the ad (simplified version)
async function rewardUserForWatchingAd() {
  const userAddress = await connectWallet();  // Get the user's wallet address
  if (!userAddress) return;

  const rewardAmount = 10;  // Define how many tokens you want to reward for watching the ad
  console.log(`Rewarding ${rewardAmount} tokens to ${userAddress}`);  // Debugging log

  // Validate Ethereum address format (basic regex validation)
  if (!isValidEthereumAddress(userAddress)) {
    console.error('Invalid Ethereum address:', userAddress);
    alert('Invalid Ethereum address. Please try again!');
    return;
  }

  // Get contract instance
  const web3 = new Web3(window.ethereum);
  const contract = new web3.eth.Contract(CONTRACT_ABI, CONTRACT_ADDRESS);
  const accounts = await web3.eth.getAccounts();
  const senderAddress = accounts[0];  // Sender address (from Metamask)

  try {
    // Interact with the smart contract (reward function)
    await contract.methods.rewardUser(userAddress, rewardAmount).send({ from: senderAddress });
    alert(`You have been rewarded ${rewardAmount} tokens!`);
    document.getElementById('reward-message').style.display = 'block';  // Show reward message
  } catch (error) {
    console.error('Error rewarding user:', error);
    alert("Error while rewarding user.");
  }
}

// Simple Ethereum address validation (basic regex)
function isValidEthereumAddress(address) {
  console.log('Validating Ethereum Address:', address);
  return /^0x[a-fA-F0-9]{40}$/.test(address);  // Simple regex to check for valid address format
}

// Watch ad click to trigger the reward
document.getElementById('ad-thumbnail').addEventListener('click', rewardUserForWatchingAd);